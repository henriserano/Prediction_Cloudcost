#!/usr/bin/env bash
# bootstrap-backend.sh — one-shot provisioner for the Terraform remote backend.
#
# What it does (idempotent — safe to re-run):
#   1. Reads the current AWS account + region.
#   2. Ensures an S3 bucket exists for the tfstate (versioning + AES-256 SSE +
#      public-access block enabled).
#   3. Ensures a DynamoDB table exists for state locking.
#   4. Writes ``terraform/backend.hcl`` with the resolved names (gitignored).
#   5. Runs ``terraform init -reconfigure -backend-config=backend.hcl`` and, if
#      a local tfstate is present, invokes ``-migrate-state`` instead so
#      pre-existing infrastructure is preserved.
#
# Usage:
#   ./terraform/bootstrap-backend.sh [--region eu-west-1] [--project-slug finops]
#
# After this runs once per AWS account, subsequent ``./deploy.sh`` calls work
# because the backend metadata is stored under .terraform/ locally.
set -euo pipefail

REGION="eu-west-1"
PROJECT_SLUG="finops"

while [[ $# -gt 0 ]]; do
  case $1 in
    --region)        REGION="$2";        shift 2 ;;
    --project-slug)  PROJECT_SLUG="$2";  shift 2 ;;
    -h|--help)
      grep '^#' "$0" | sed 's/^# \{0,1\}//'
      exit 0 ;;
    *) echo "Unknown argument: $1"; exit 1 ;;
  esac
done

fail() { echo "❌ $*" >&2; exit 1; }
info() { echo "→ $*"; }

# ── Preflight ─────────────────────────────────────────────────────────────────
for c in aws terraform jq; do
  command -v "$c" >/dev/null 2>&1 || fail "'$c' is required but not in PATH."
done

ACCOUNT_ID=$(aws sts get-caller-identity --region "$REGION" --query 'Account' --output text 2>/dev/null) \
  || fail "AWS credentials are not configured for region ${REGION}. Run 'aws configure' or export AWS_* env vars."

# Names — deterministic per account+project so a re-run finds the same
# resources. S3 bucket names must be globally unique; suffixing with the
# account ID keeps collisions impossible without leaking anything sensitive
# (account IDs are not secrets).
BUCKET="${PROJECT_SLUG}-tfstate-${ACCOUNT_ID}-${REGION}"
LOCK_TABLE="${PROJECT_SLUG}-tflock"

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "  Bootstrap Terraform remote backend"
echo "  account : ${ACCOUNT_ID}"
echo "  region  : ${REGION}"
echo "  bucket  : ${BUCKET}"
echo "  table   : ${LOCK_TABLE}"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"

# ── S3 bucket ─────────────────────────────────────────────────────────────────
if aws s3api head-bucket --bucket "$BUCKET" --region "$REGION" 2>/dev/null; then
  info "S3 bucket ${BUCKET} already exists — skipping create."
else
  info "Creating S3 bucket ${BUCKET}…"
  if [[ "$REGION" == "us-east-1" ]]; then
    # us-east-1 rejects the LocationConstraint arg by design.
    aws s3api create-bucket --bucket "$BUCKET" --region "$REGION" >/dev/null
  else
    aws s3api create-bucket \
      --bucket "$BUCKET" --region "$REGION" \
      --create-bucket-configuration "LocationConstraint=${REGION}" >/dev/null
  fi
fi

info "Enabling versioning on ${BUCKET}…"
aws s3api put-bucket-versioning \
  --bucket "$BUCKET" --region "$REGION" \
  --versioning-configuration Status=Enabled >/dev/null

info "Enforcing AES256 server-side encryption on ${BUCKET}…"
aws s3api put-bucket-encryption \
  --bucket "$BUCKET" --region "$REGION" \
  --server-side-encryption-configuration '{"Rules":[{"ApplyServerSideEncryptionByDefault":{"SSEAlgorithm":"AES256"}}]}' \
  >/dev/null

info "Blocking public access on ${BUCKET}…"
aws s3api put-public-access-block \
  --bucket "$BUCKET" --region "$REGION" \
  --public-access-block-configuration \
    "BlockPublicAcls=true,IgnorePublicAcls=true,BlockPublicPolicy=true,RestrictPublicBuckets=true" \
  >/dev/null

# ── DynamoDB lock table ───────────────────────────────────────────────────────
if aws dynamodb describe-table --table-name "$LOCK_TABLE" --region "$REGION" >/dev/null 2>&1; then
  info "DynamoDB table ${LOCK_TABLE} already exists — skipping create."
else
  info "Creating DynamoDB lock table ${LOCK_TABLE}…"
  aws dynamodb create-table \
    --table-name "$LOCK_TABLE" --region "$REGION" \
    --attribute-definitions AttributeName=LockID,AttributeType=S \
    --key-schema AttributeName=LockID,KeyType=HASH \
    --billing-mode PAY_PER_REQUEST \
    --sse-specification Enabled=true >/dev/null
  info "Waiting for ${LOCK_TABLE} to become ACTIVE…"
  aws dynamodb wait table-exists --table-name "$LOCK_TABLE" --region "$REGION"
fi

# ── backend.hcl (gitignored) ──────────────────────────────────────────────────
BACKEND_HCL="$(cd "$(dirname "$0")" && pwd)/backend.hcl"
info "Writing ${BACKEND_HCL}…"
cat > "$BACKEND_HCL" <<HCL
# Auto-generated by bootstrap-backend.sh — do NOT commit.
bucket         = "${BUCKET}"
key            = "finops-backend/terraform.tfstate"
region         = "${REGION}"
dynamodb_table = "${LOCK_TABLE}"
encrypt        = true
HCL

# ── terraform init ────────────────────────────────────────────────────────────
cd "$(dirname "$0")"

# If a local tfstate is present, we want -migrate-state so pre-existing infra
# is preserved when the backend switches from local to s3. Otherwise the
# faster -reconfigure is enough.
if [[ -f terraform.tfstate ]]; then
  info "Local terraform.tfstate detected — migrating to the S3 backend…"
  terraform init -migrate-state -force-copy -backend-config="$BACKEND_HCL"
else
  info "No local tfstate — running fresh init…"
  terraform init -reconfigure -backend-config="$BACKEND_HCL"
fi

echo ""
echo "✅ Backend bootstrap complete."
echo "   Subsequent ./deploy.sh runs will use s3://${BUCKET} for state and"
echo "   ${LOCK_TABLE} for locking. Share the (non-secret) bucket/table names"
echo "   with the team; each teammate runs this same script once."
